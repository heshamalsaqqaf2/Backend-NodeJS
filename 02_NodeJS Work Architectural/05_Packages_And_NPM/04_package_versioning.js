/** 
 * @Install_Packages_With_NPM
 *  npm install will always install the latest stable version.
 *  We can specify the version number when we install a package
 *  if we want to install a specific version of the package. To do that we 
 *?  run npm install package_name@package_version.
 * 
 *  NPM follows semantic versioning convention //* (e.g. 1.2.1)
 *  with the first digit representing major version that is not backward compatible,
 *  second digit representing minor version that has new functionalities
 *  but is backward compatible, and the third digit representing
 *  a patch version with a bug fix.
*/